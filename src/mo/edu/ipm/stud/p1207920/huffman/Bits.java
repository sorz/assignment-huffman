package mo.edu.ipm.stud.p1207920.huffman;


public class Bits {
    private int bits;
    private int length;

    public Bits(int bits, int length) {
        this.bits = bits;
        this.length = length;
    }

    public int getBits() {
        return bits;
    }

    public int getLength() {
        return length;
    }
}
