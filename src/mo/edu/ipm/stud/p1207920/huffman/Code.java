package mo.edu.ipm.stud.p1207920.huffman;


public class Code {
    private int code;
    private int length;

    public Code(int code, int length) {
        this.code = code;
        this.length = length;
    }

    public int getCode() {
        return code;
    }

    public int getLength() {
        return length;
    }
}
